class Encoder(torch.nn.Module):
    def __init__(self, w):
        super().__init__();  self.enc = w.encoder
    def forward(self, mel):                   # ‚Üê encoder needs ONLY the Mel
        return self.enc(mel)

class Decoder(torch.nn.Module):
    def __init__(self, w):
        super().__init__();  self.dec = w.decoder
    def forward(self, tokens, enc_out):       # ‚Üê decoder uses tokens + enc_out
        return self.dec(tokens, enc_out)

enc = Encoder(model).eval()
dec = Decoder(model).eval()

